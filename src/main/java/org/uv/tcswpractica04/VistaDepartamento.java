    // Botón Eliminar
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {
        int fila = jTable4.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un departamento", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            DefaultTableModel modelo = (DefaultTableModel) jTable4.getModel();
            Object claveObj = modelo.getValueAt(fila, 0);
            Long clave;
            if (claveObj instanceof Long) {
                clave = (Long) claveObj;
            } else if (claveObj instanceof Integer) {
                clave = ((Integer) claveObj).longValue();
            } else {
                clave = Long.valueOf(String.valueOf(claveObj));
            }
            Departamento encontrado = daoDepartamento.findById(clave);
            if (encontrado == null) {
                JOptionPane.showMessageDialog(this, "No se encontró el departamento", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            int opcion = JOptionPane.showConfirmDialog(this,
                    "¿Eliminar el departamento '" + encontrado.getNombre() + "'?",
                    "Confirmar", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            if (opcion == JOptionPane.YES_OPTION) {
                Departamento eliminado = daoDepartamento.delete(clave);
                if (eliminado != null) {
                    JOptionPane.showMessageDialog(this, "Departamento eliminado", "Éxito", JOptionPane.INFORMATION_MESSAGE);
                    listarDepartamentos();
                    limpiarCampos();
                } else {
                    JOptionPane.showMessageDialog(this, "No se pudo eliminar", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al eliminar: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

