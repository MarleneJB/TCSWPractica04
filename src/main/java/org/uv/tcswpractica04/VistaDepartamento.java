    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        try {
            String nombre = jTextField1.getText().trim();
            if (nombre.isEmpty()) {
                JOptionPane.showMessageDialog(this, "El nombre es obligatorio", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            if (editandoDepartamento) {
                Departamento pojo = new Departamento();
                pojo.setNombre(nombre);
                Departamento actualizado = daoDepartamento.update(pojo, idDepartamentoEditando);
                if (actualizado != null) {
                    JOptionPane.showMessageDialog(this, "Departamento actualizado", "Éxito", JOptionPane.INFORMATION_MESSAGE);
                    limpiarCampos();
                    listarDepartamentos();
                } else {
                    JOptionPane.showMessageDialog(this, "No se encontró el departamento", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                Departamento depto = new Departamento();
                depto.setNombre(nombre);

                boolean creado = daoDepartamento.create(depto);
                if (creado) {
                    JOptionPane.showMessageDialog(this, "Departamento creado", "Éxito", JOptionPane.INFORMATION_MESSAGE);
                    limpiarCampos();
                    listarDepartamentos();
                } else {
                    JOptionPane.showMessageDialog(this, "No se pudo crear el departamento", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, (editandoDepartamento ? "Error al actualizar: " : "Error al crear: ") + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int fila = jTable4.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un departamento", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            DefaultTableModel modelo = (DefaultTableModel) jTable4.getModel();
            Object claveObj = modelo.getValueAt(fila, 0);
            Long clave;
            if (claveObj instanceof Long) {
                clave = (Long) claveObj;
            } else if (claveObj instanceof Integer) {
                clave = ((Integer) claveObj).longValue();
            } else {
                clave = Long.valueOf(String.valueOf(claveObj));
            }

            Departamento encontrado = daoDepartamento.findById(clave);
            if (encontrado != null) {
                jTextField1.setText(encontrado.getNombre());
                editandoDepartamento = true;
                idDepartamentoEditando = encontrado.getClave();
                btnAgregar.setText("Actualizar");
                JOptionPane.showMessageDialog(this, "Departamento cargado para edición", "Información", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "No se encontró el departamento", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar departamento: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

