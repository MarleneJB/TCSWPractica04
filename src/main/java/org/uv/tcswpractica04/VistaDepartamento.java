    private void listarDepartamentos() {
        try {
            List<Departamento> departamentos = daoDepartamento.findAll();
            DefaultTableModel modelo = (DefaultTableModel) jTable4.getModel();
            modelo.setRowCount(0);
            for (Departamento d : departamentos) {
                Object[] fila = { d.getClave(), d.getNombre() };
                modelo.addRow(fila);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al listar: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void limpiarCampos() {
        jTextField1.setText("");
        jTable4.clearSelection();
        idDepartamentoEditando = null;
        editandoDepartamento = false;
        btnAgregar.setText("Agregar");
    }

